export async function fetchNotificationStats(): Promise<{
  totalSent: number;
  totalDrafts: number;
  avgDeliveryRate: number;
  avgOpenRate: number;
  totalRecipients: number;
  sentThisMonth: number;
  lastSent: AppNotification | null;
  byPriority: Record<NotificationPriority, number>;
  byEventType: Record<string, number>;
}> {
  const all = await fetchNotifications();
  const sent = all.filter((n) => !n.isDraft && n.status !== 'draft');
  const drafts = all.filter((n) => n.isDraft);
  const now = new Date();
  const monthStart = new Date(now.getFullYear(), now.getMonth(), 1);

  const sentThisMonth = sent.filter((n) => n.schedule.sentAt && n.schedule.sentAt >= monthStart).length;
  const withDelivery = sent.filter((n) => n.delivery);
  const avgDeliveryRate =
    withDelivery.length > 0
      ? withDelivery.reduce((s, n) => s + (n.delivery?.deliveryRate ?? 0), 0) / withDelivery.length
      : 0;
  const avgOpenRate =
    withDelivery.length > 0
      ? withDelivery.reduce((s, n) => s + (n.delivery?.openRate ?? 0), 0) / withDelivery.length
      : 0;
  const totalRecipients = sent.reduce((s, n) => s + (n.delivery?.totalTargeted ?? 0), 0);

  const byPriority: Record<NotificationPriority, number> = { Low: 0, Normal: 0, High: 0, Urgent: 0 };
  const byEventType: Record<string, number> = {};
  for (const n of sent) {
    byPriority[n.priority] += 1;
    byEventType[n.eventType] = (byEventType[n.eventType] ?? 0) + 1;
  }

  const lastSent = sent.sort((a, b) => (b.schedule.sentAt?.getTime() ?? 0) - (a.schedule.sentAt?.getTime() ?? 0))[0] ?? null;

  return {
    totalSent: sent.length,
    totalDrafts: drafts.length,
    avgDeliveryRate,
    avgOpenRate,
    totalRecipients,
    sentThisMonth,
    lastSent,
    byPriority,
    byEventType,
  };
}

export async function recoverStuckSendingNotifications(): Promise<number> {
  if (broadcastTableUnavailable) return 0;

  try {
    const { client } = await requireSession();
    const cutoff = new Date(Date.now() - 5 * 60 * 1000).toISOString();
    const { data, error } = await client
      .from('broadcast_notifications')
      .select('id')
      .eq('status', 'sending')
      .lt('updated_at', cutoff);
    if (error) {
      if (isMissingBroadcastTable(error)) {
        broadcastTableUnavailable = true;
        return 0;
      }
      throw error;
    }
    if (!data?.length) return 0;

    const ids = data.map((r) => String(r.id));
    const { error: updateErr } = await client
      .from('broadcast_notifications')
      .update({ status: 'partially_failed', updated_at: new Date().toISOString() })
      .in('id', ids);
    if (updateErr) throw updateErr;
    return ids.length;
  } catch {
    return 0;
  }
}

export function subscribeToNotifications(callback: (notifications: AppNotification[]) => void): () => void {
  if (notificationsRealtimeFailed || broadcastTableUnavailable) {
    void fetchNotifications().then(callback).catch(() => undefined);
    return () => {};
  }

  const client = getSupabase();
  notificationsRealtimeListeners.add(callback);

  if (!notificationsRealtimeChannel) {
    notificationsRealtimeChannel = client
      .channel('admin-broadcast-notifications')
      .on('postgres_changes', { event: '*', schema: 'public', table: 'broadcast_notifications' }, () => {
        void notifyNotificationsRealtimeListeners();
      })
      .subscribe((status) => {
        if (status === 'CHANNEL_ERROR' || status === 'TIMED_OUT') {
          notificationsRealtimeFailed = true;
        }
      });
  }

  void fetchNotifications().then(callback).catch(() => undefined);

  return () => {
    notificationsRealtimeListeners.delete(callback);
    if (notificationsRealtimeListeners.size === 0 && notificationsRealtimeChannel) {
      void client.removeChannel(notificationsRealtimeChannel);
      notificationsRealtimeChannel = null;
    }
  };
}

