type BroadcastRow = Record<string, unknown>;

type PostgrestErrorLike = { code?: string; message?: string };

function isMissingBroadcastTable(error: PostgrestErrorLike): boolean {
  return (
    error.code === 'PGRST205' &&
    (error.message?.includes('broadcast_notifications') ?? false)
  );
}

const notificationsRealtimeListeners = new Set<(notifications: AppNotification[]) => void>();
let notificationsRealtimeChannel: RealtimeChannel | null = null;
let notificationsRealtimeFailed = false;
let broadcastTableUnavailable = false;

async function notifyNotificationsRealtimeListeners() {
  try {
    const notifications = await fetchNotifications();
    for (const listener of notificationsRealtimeListeners) {
      listener(notifications);
    }
  } catch {
    // polling fallback handles refresh
  }
}

async function requireSession() {
  const client = getSupabase();
  const { data, error } = await client.auth.getSession();
  if (error) throw error;
  if (!data.session) throw new Error('Please sign in to manage notifications.');
  return { client, session: data.session };
}

function parseDate(value: unknown): Date | null {
  if (!value) return null;
  const d = new Date(String(value));
  return Number.isNaN(d.getTime()) ? null : d;
}

function mapRowToAudience(row: BroadcastRow): AudienceConfig {
  return {
    type: String(row.audience_type ?? 'all_users') as AudienceConfig['type'],
    planId: row.audience_plan_id ? String(row.audience_plan_id) : undefined,
    planName: row.audience_plan_name ? String(row.audience_plan_name) : undefined,
    area: row.audience_area ? String(row.audience_area) : undefined,
    userIds: Array.isArray(row.audience_user_ids)
      ? (row.audience_user_ids as string[]).map(String)
      : undefined,
    userNames: Array.isArray(row.audience_user_names)
      ? (row.audience_user_names as string[]).map(String)
      : undefined,
    officerIds: Array.isArray(row.audience_officer_ids)
      ? (row.audience_officer_ids as string[]).map(String)
      : undefined,
    estimatedCount: Number(row.audience_estimated_count ?? 0),
  };
}

function mapRowToDelivery(row: BroadcastRow): DeliveryStats | null {
  if (row.is_draft) return null;
  const totalSent = Number(row.total_sent ?? 0);
  const totalDelivered = Number(row.total_delivered ?? 0);
  return {
    totalTargeted: Number(row.total_targeted ?? 0),
    totalSent,
    totalDelivered,
    totalFailed: Number(row.total_failed ?? 0),
    totalOpened: Number(row.total_opened ?? 0),
    failedTokens: Array.isArray(row.failed_tokens) ? (row.failed_tokens as string[]) : [],
    deliveryRate: Number(row.delivery_rate ?? 0),
    openRate: Number(row.open_rate ?? 0),
    processingMs: Number(row.processing_ms ?? 0),
  };
}

export function mapRowToAppNotification(row: BroadcastRow, recipients: NotificationRecipient[] = []): AppNotification {
  return {
    id: String(row.id),
    title: String(row.title ?? ''),
    message: String(row.message ?? ''),
    priority: String(row.priority ?? 'Normal') as NotificationPriority,
    eventType: String(row.event_type ?? 'none') as EventType,
    status: String(row.status ?? 'draft') as NotificationStatus,
    audience: mapRowToAudience(row),
    schedule: {
      isScheduled: Boolean(row.is_scheduled),
      scheduledAt: parseDate(row.scheduled_at),
      timezone: String(row.timezone ?? 'Asia/Kolkata'),
      sentAt: parseDate(row.sent_at),
    },
    delivery: mapRowToDelivery(row),
    recipients,
    createdAt: parseDate(row.created_at) ?? new Date(),
    updatedAt: parseDate(row.updated_at) ?? new Date(),
    createdById: String(row.created_by_id ?? ''),
    createdByName: String(row.created_by_name ?? ''),
    isDraft: Boolean(row.is_draft),
    isAutoGenerated: Boolean(row.is_auto_generated),
    tags: Array.isArray(row.tags) ? (row.tags as string[]) : [],
    deepLinkUrl: row.deep_link_url ? String(row.deep_link_url) : undefined,
    imageUrl: row.image_url ? String(row.image_url) : undefined,
    linkedRequestId: row.linked_request_id ? String(row.linked_request_id) : undefined,
    linkedTicketId: row.linked_ticket_id ? String(row.linked_ticket_id) : undefined,
    linkedPlanId: row.linked_plan_id ? String(row.linked_plan_id) : undefined,
  };
}

async function fetchRecipientsForNotification(notificationId: string): Promise<NotificationRecipient[]> {
  const { client } = await requireSession();
  const { data, error } = await client
    .from('notification_recipients')
    .select('*')
    .eq('notification_id', notificationId)
    .order('created_at', { ascending: true });
  if (error) throw error;

  return (data ?? []).map((row) => ({
    userId: String(row.user_id),
    userName: String(row.user_name),
    userType: String(row.user_type) as NotificationRecipient['userType'],
    pushToken: row.push_token ? String(row.push_token) : null,
    deliveryStatus: String(row.delivery_status ?? 'pending') as NotificationRecipient['deliveryStatus'],
    failureReason: row.failure_reason ? String(row.failure_reason) : undefined,
    sentAt: parseDate(row.sent_at) ?? undefined,
  }));
}

export async function fetchNotifications(): Promise<AppNotification[]> {
  if (broadcastTableUnavailable) return [];

  const { client } = await requireSession();
  const { data, error } = await client
    .from('broadcast_notifications')
    .select('*')
    .order('created_at', { ascending: false });
  if (error) {
    if (isMissingBroadcastTable(error)) {
      broadcastTableUnavailable = true;
      return [];
    }
    throw error;
  }
  broadcastTableUnavailable = false;
  return (data ?? []).map((row) => mapRowToAppNotification(row as BroadcastRow));
}

export async function fetchDrafts(): Promise<AppNotification[]> {
  const all = await fetchNotifications();
  return all.filter((n) => n.isDraft);
}

export async function fetchSentHistory(): Promise<AppNotification[]> {
  const all = await fetchNotifications();
  return all.filter((n) => !n.isDraft);
}

export async function fetchNotificationById(id: string): Promise<AppNotification> {
  const { client } = await requireSession();
  const { data, error } = await client.from('broadcast_notifications').select('*').eq('id', id).maybeSingle();
  if (error) throw error;
  if (!data) throw new Error('Notification not found');
  const recipients = await fetchRecipientsForNotification(id);
  return mapRowToAppNotification(data as BroadcastRow, recipients);
}

export async function createNotification(
  data: CreateNotificationFormData,
  admin: { id: string; name: string },
): Promise<AppNotification> {
  const { client } = await requireSession();
  const estimatedCount = await resolveAudienceCount(data.audience);
  const payload = formDataToDbPayload(data, admin, {
    audience_estimated_count: estimatedCount,
    is_draft: true,
    status: 'draft',
  });

  const { data: row, error } = await client.from('broadcast_notifications').insert(payload).select('*').single();
  if (error) throw error;
  return mapRowToAppNotification(row as BroadcastRow);
}

export async function updateNotification(
  id: string,
  data: Partial<CreateNotificationFormData>,
  admin: { id: string; name: string },
): Promise<AppNotification> {
  const { client } = await requireSession();
  const estimatedCount = data.audience ? await resolveAudienceCount(data.audience) : undefined;
  const payload = formDataToDbPayload(data, admin, {
    ...(estimatedCount !== undefined ? { audience_estimated_count: estimatedCount } : {}),
  });

  const { data: row, error } = await client
    .from('broadcast_notifications')
    .update(payload)
    .eq('id', id)
    .select('*')
    .single();
  if (error) throw error;
  return mapRowToAppNotification(row as BroadcastRow);
}

export async function saveDraft(
  data: CreateNotificationFormData,
  admin: { id: string; name: string },
  existingId?: string,
): Promise<AppNotification> {
  if (existingId) {
    const updated = await updateNotification(existingId, data, admin);
    await getSupabase()
      .from('broadcast_notifications')
      .update({ is_draft: true, status: 'draft' })
      .eq('id', existingId);
    return { ...updated, isDraft: true, status: 'draft' };
  }
  return createNotification(data, admin);
}

